// Javascript is dynamically typed language.

//(1) Number
let num=10;
console.log(num);

//(2) Null
let x=null;
console.log(x);
console.log(typeof(x));

//(3) String
let fullName="Rupali";
console.log(fullName);
console.log(typeof(fullName));

//(4) symbol
let y=Symbol("hello");
console.log(y);
console.log(typeof(y));

//(5) Boolean
let isPass = true;
console.log(isPass);

//(6) BigInt
let a=BigInt("123");
console.log(a);

//(7) Undefined
let b;
console.log(b);



